<h2 id="html5" tabindex="-1"><a class="header-anchor" href="#html5"><span>HTML5</span></a></h2>
<p>HTML5 是 HTML 现行的最新修订版本，主要的新特性包括:</p>
<ul>
<li>用于绘画的 <code v-pre>&lt;canvas /&gt;</code> 元素</li>
<li>用于媒体播放的 <code v-pre>&lt;video /&gt;</code> 和 <code v-pre>&lt;audio /&gt;</code> 元素</li>
<li>更好地支持本地离线存储</li>
<li>新的特殊内容元素，比如 <code v-pre>&lt;article /&gt;</code>、<code v-pre>&lt;footer /&gt;</code>、<code v-pre>&lt;header /&gt;</code>、<code v-pre>&lt;nav /&gt;</code>、<code v-pre>&lt;section /&gt;</code></li>
<li>新的表单控件，比如 calendar、date、time、email、url、search</li>
</ul>
